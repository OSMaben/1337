char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z')
			&& (str[i - 1] == ' ' || str[i - 1] == '-' || str[i - 1] == '+' || i == 0))
		{
			str[i] -= 32;
		}
		i++;
	}
	return (str);
}
